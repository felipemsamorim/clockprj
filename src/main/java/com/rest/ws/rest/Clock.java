package com.rest.ws.rest;

public class Clock {
private int hour = 0;
private int minute = 0;
private float angle = 0;
public float getAngle() {
	return angle;
}
public void setAngle(float angle) {
	this.angle = angle;
}
public int getHour() {
	return hour;
}
public void setHour(int hour) {
	this.hour = hour;
}
public int getMinute() {
	return minute;
}
public void setMinute(int minute) {
	this.minute = minute;
}
}
